pub fn run(){

    // Integers
    let x: i32 = -42;
    let y: u64 = 100;

    println!("Signed integer: {}", x);
    println!("Unsigned integer: {}", y);

    // Floating point
    let f: f32 = -3.14;
    println!("Floating point number: {}", f);

    // Boolean type
    let is_raining: bool = false;
    println!("Current conditions are rainy? {}", is_raining);

    // Character type
    let c: char = 'x';
    println!("Character type : {}", c);

}